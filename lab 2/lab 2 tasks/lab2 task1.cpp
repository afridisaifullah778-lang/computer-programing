#include <iostream>
using namespace std;

int main()
{
    int i = 1;   // Initialize variable starting from 1

    // while loop runs until i is less than or equal to 10
    while(i <= 10)
    {
        cout << i << endl;   // Display number on separate line
        i++;                 // Increment value of i
    }

    return 0;   // End of program
}